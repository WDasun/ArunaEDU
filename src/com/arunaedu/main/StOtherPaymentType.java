    private void btn_saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_saveMouseClicked
        int status = (rad_active.isSelected()) ? 1 : 0;

        if (checkFields()) {

            try {
                SAVE("stdother_paymenttype", "'"
                        + txt_paymentid.getText() + "','"
                        + txt_description.getText() + "','"
                        + Double.parseDouble(txt_amount.getText()) + "','"
                        + status + "'");
                JOptionPane.showMessageDialog(this, " Saved !");
                clearFields();
 if(cbox_showall.isSelected()){
        removeAllRows(jTable1);
        fillTable();
        }
            } catch (SQLIntegrityConstraintViolationException ex) {
                JOptionPane.showMessageDialog(this, "Payment ID has already in the DataBase !. The Error Message: " + ex.getMessage());
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Fill all Fields !");
        }
    }//GEN-LAST:event_btn_saveMouseClicked

    private void btn_updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_updateMouseClicked
        int status = (rad_active.isSelected()) ? 1 : 0;
        if (checkFields()) {
            try {
                ResultSet rs = DB.search("select count(1) as rowCount from stdother_paymenttype where id_paymenttype='" + txt_paymentid.getText() + "'");
                rs.next();
                if (rs.getInt("rowCount") != 0) {
                
                UPDATE("stdother_paymenttype",
                        "description='" + txt_description.getText()
                        + "',amount='" + Double.parseDouble(txt_amount.getText())
                        + "',status='" + status
                        + "'", "id_paymenttype",
                        txt_paymentid.getText());
                JOptionPane.showMessageDialog(this, " Updated !");
                 if(cbox_showall.isSelected()){
        removeAllRows(jTable1);
        fillTable();
        }
                 } else {
                    JOptionPane.showMessageDialog(this, "Payment Type ID is not same !");
                }

            } catch (SQLIntegrityConstraintViolationException ex) {
                JOptionPane.showMessageDialog(this, "Payment ID has already in the DataBase !. The Error Message: " + ex.getMessage());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Fill all Fields !");
        }
    }//GEN-LAST:event_btn_updateMouseClicked


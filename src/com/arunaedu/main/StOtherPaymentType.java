    private void btn_getSelectedMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_getSelectedMouseClicked
        txt_paymentid.setText((String) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
        try {
            SEARCH("stdother_paymenttype", "id_paymenttype", (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            ResultSet rs = getResult();

            if (rs.next()) {
                txt_paymentid.setText(rs.getString("id_paymenttype"));
                txt_description.setText(rs.getString("description"));
                txt_amount.setText(Double.toString(rs.getDouble("amount")));

                if (rs.getBoolean("status")) {
                    rad_active.setSelected(true);
                } else {
                    rad_inactive.setSelected(true);
                }

                for (int i = 0; i < jf.size(); i++) {
                    textFieldTextType(0, jf.get(i));
                    if (jf.get(i).getText().equals("")) {
                        jf.get(i).setBackground(new java.awt.Color(253, 237, 237));
                    } else {
                        jf.get(i).setBackground(new java.awt.Color(178, 230, 245));
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_getSelectedMouseClicked


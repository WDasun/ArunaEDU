    private void removeAllRows(JTable j) {
        for (int i = j.getRowCount() - 1; i >= 0; i--) {
            ((DefaultTableModel) j.getModel()).removeRow(i);
        }
    }

    private void fillTable() {
        try {
            SEARCH("stdother_paymenttype");
            fillTableProcess();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void fillTableProcess() {

        try {

            ResultSet rs = getResult();
            while (rs.next()) {

                String id_paymenttype = rs.getString("id_paymenttype");
                String description = rs.getString("description");
                String amount = Double.toString(rs.getDouble("amount"));
                boolean status = rs.getBoolean("status");

                Vector v = new Vector();

                v.add(id_paymenttype);
                v.add(description);
                v.add(amount);
                if (status) {
                    v.add("Active");
                } else {
                    v.add("Inactive");
                }

                dtm1 = (DefaultTableModel) jTable1.getModel();
                dtm1.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }


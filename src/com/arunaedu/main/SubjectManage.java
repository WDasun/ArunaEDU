    private void btn_saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_saveMouseClicked

        int status = (rad_active.isSelected()) ? 1 : 0;

        if (checkFields()) {
            try {
                SAVE("subject", "'"
                        + txt_subjectid.getText() + "','"
                        + txt_description.getText() + "','"
                        + Integer.valueOf(txt_contractPersonsLimit.getText()) + "','"
                        + status + "'");
                JOptionPane.showMessageDialog(this, " Saved !");
                clearFields();
                removeAllRows();
                fillTable();

                txt_subjectid.grabFocus();
            } catch (SQLIntegrityConstraintViolationException ex) {
                JOptionPane.showMessageDialog(this, "subject ID has already in the DataBase !. The Error Message: " + ex.getMessage());
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Fill all Fields !");
        }
    }//GEN-LAST:event_btn_saveMouseClicked


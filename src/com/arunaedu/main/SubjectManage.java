    private boolean checkFields() {

        if (txt_subjectid.getText().equals("")
                && txt_subjectid.getText().equals("")
                && txt_description.getText().equals("")
                && txt_contractPersonsLimit.getText().equals("")) {
            return false;
        } else {
            return true;
        }

    }

    private void clearFields() {
        setDefaultText();
        setDefaultTextField();
        txt_description.setText("");
        rad_active.setSelected(true);
        txt_subjectid.grabFocus();
    }

    private void removeAllRows() {
        for (int i = Table_1.getRowCount() - 1; i >= 0; i--) {
            ((DefaultTableModel) Table_1.getModel()).removeRow(i);
        }
    }

    private void fillTable() {
        try {
            SEARCH("subject");
            fillTableProcess();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void fillTableProcess() {

        try {

            ResultSet rs = getResult();
            while (rs.next()) {

                String id_subject = rs.getString("id_subject");
                String description = rs.getString("description");
                String maxcontracts_number = rs.getString("maxcontracts_number");

                boolean status = rs.getBoolean("status");

                Vector v = new Vector();

                v.add(id_subject);
                v.add(description);
                v.add(maxcontracts_number);

                if (status) {
                    v.add("Active");
                } else {
                    v.add("Inactive");
                }

                dtm1 = (DefaultTableModel) Table_1.getModel();
                dtm1.addRow(v);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


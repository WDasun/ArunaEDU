    private void btn_updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_updateMouseClicked

        int status = (rad_active.isSelected()) ? 1 : 0;

        if (checkFields()) {

            try {
                ResultSet rs = DB.search("select count(1) as rowCount from subject where id_subject='" + txt_subjectid.getText() + "'");
                rs.next();
                if (rs.getInt("rowCount") != 0) {
                    UPDATE("subject",
                            "description='" + txt_description.getText()
                            + "',maxcontracts_number='" + Integer.valueOf(txt_contractPersonsLimit.getText())
                            + "',status='" + status
                            + "'", "id_subject",
                            txt_subjectid.getText());

                    JOptionPane.showMessageDialog(this, " Updated !");
                    removeAllRows();
                    fillTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Subject ID is not same !");
                }

            } catch (SQLIntegrityConstraintViolationException ex) {
                JOptionPane.showMessageDialog(this, "subject ID has already in the DataBase !. The Error Message: " + ex.getMessage());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Fill all Fields !");
        }

    }//GEN-LAST:event_btn_updateMouseClicked


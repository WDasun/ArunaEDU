    private void txt_searchBoxKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchBoxKeyReleased
        if (cbox_showSuggetions.isSelected()) {
            removeAllRows();
            if (cbox_fname.isSelected()) {
                try {

                    for (int j = 0; j < mylist.size(); j++) {
                        int s = txt_searchBox.getText().length();
                        if (mylist.get(j).substring(0, s).equals(txt_searchBox.getText())) {

                            SEARCH("student", "id_student", id.get(j));
                            fillTableProcess();
                        }
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }

            } else if (cbox_nic.isSelected()) {
                try {
                    LIKESEARCH("student", "nic", txt_searchBox.getText());
                    fillTableProcess();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            } else if (cbox_studentId.isSelected()) {
                try {
                    LIKESEARCH("student", "id_student", txt_searchBox.getText());
                    fillTableProcess();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_txt_searchBoxKeyReleased


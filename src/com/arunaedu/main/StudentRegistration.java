    private void btn_editSelectedMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_editSelectedMouseClicked
        txt_studentid.setText((String) Table_1.getValueAt(Table_1.getSelectedRow(), 0));
        try {
            SEARCH("student", "id_student", (String) Table_1.getValueAt(Table_1.getSelectedRow(), 0));
            ResultSet rs = getResult();

            if (rs.next()) {
                txt_fname.setText(rs.getString("fname"));
                txt_lname.setText(rs.getString("lname"));
                txt_nic.setText(rs.getString("nic"));
                txt_cno.setText(rs.getString("contact_no"));
                txt_address.setText(rs.getString("address"));
                txt_email.setText(rs.getString("email"));
                txt_ffname.setText(rs.getString("father_fname"));
                txt_flname.setText(rs.getString("father_lname"));
                txt_fcno.setText(rs.getString("father_ctn"));
                txt_mfname.setText(rs.getString("mother_fname"));
                txt_mlname.setText(rs.getString("mother_lname"));
                txt_mcno.setText(rs.getString("mother_ctn"));
                txt_gfname.setText(rs.getString("guardian_fname"));
                txt_glname.setText(rs.getString("guardian_lname"));
                txt_gcno.setText(rs.getString("guardian_ctn"));
                if (rs.getBoolean("status")) {
                    rad_active.setSelected(true);
                } else {
                    rad_inactive.setSelected(true);
                }
                if (rs.getString("gender").equals("Male")) {
                    jComboBox_gender.setSelectedIndex(0);
                } else if (rs.getString("gender").equals("Female")) {
                    jComboBox_gender.setSelectedIndex(1);
                } else {
                    jComboBox_gender.setSelectedIndex(2);
                }
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String text = rs.getString("dob");
                Date parse = sdf.parse(text);
                jDateChooser_dob.setDate(parse);

                replacepath = rs.getString("imagepath");
                File file = new File(replacepath);
                Image image = ImageIO.read(file);
                image = image.getScaledInstance(lbl_image.getWidth(), lbl_image.getHeight(), Image.SCALE_SMOOTH);
                lbl_image.setIcon(new ImageIcon(image));

                jTabbedPane1.setSelectedIndex(0);

                for (int i = 0; i < jf.size(); i++) {
                    textFieldTextType(0, jf.get(i));
                    if (jf.get(i).getText().equals("")) {
                        jf.get(i).setBackground(new java.awt.Color(253, 237, 237));
                    } else {
                        jf.get(i).setBackground(new java.awt.Color(178, 230, 245));
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_btn_editSelectedMouseClicked


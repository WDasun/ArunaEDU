    private void btn_saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_saveMouseClicked

        if (checkFields()) {
            int status = (rad_active.isSelected()) ? 1 : 0;

                    try {
                        if(STATUSSEARCH("lecturer_contract", "id_contract", txt_contractId.getText())){
                        SAVE("class", "'"
                            + txt_classId.getText() + "','"
                            + txt_description.getText() + "','"
                            + txt_contractId.getText() + "','"
                            + Double.parseDouble(txt_classFees.getText()) + "','"
                            + status + "'");
                        JOptionPane.showMessageDialog(this, " Saved !");
                        clearFields();
                        if (cbox_classShowAll.isSelected()) {
                            removeAllRows(jTable2);
                            fillTable(2);
                            genarateClassId();
                            txt_description.grabFocus();
                        }
                         }else{
                JOptionPane.showMessageDialog(this, "Contract not Active !");
            }
                    } catch (SQLIntegrityConstraintViolationException ex) {
                        JOptionPane.showMessageDialog(this, "Class ID has already in the DataBase !. The Error Message: " + ex.getMessage());
                    } catch (Exception e) {
                        e.printStackTrace();
                    }

        } else {
            JOptionPane.showMessageDialog(this, "Fill all Fields !");
        }
    }//GEN-LAST:event_btn_saveMouseClicked


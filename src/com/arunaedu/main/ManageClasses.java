    private void warnningLabel(int id, JTextField jTextField) {

        if (id == 1004) {
            if (jTextField.getFont().getSize() == 14) {
                prevText = jTextField.getText();
                jTextField.setText("");
                textFieldTextType(0, jTextField);

            }
            jTextField.setBackground(new java.awt.Color(218, 243, 250));
        } else if (id == 1005) {
            if (jTextField.getText().equals("")) {
                jTextField.setText(prevText);
                textFieldTextType(1, jTextField);
                prevText = null;
                jTextField.setBackground(new java.awt.Color(253, 237, 237));

            } else {
                prevText = null;
                jTextField.setBackground(new java.awt.Color(178, 230, 245));
            }
        }

    }
    private void removeAllRows(JTable j) {
        for (int i = j.getRowCount() - 1; i >= 0; i--) {
            ((DefaultTableModel) j.getModel()).removeRow(i);
        }
    }
    private void fillTable(int i) {
        try {
            SEARCH("class");
            fillTableProcess(i);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
     private void fillTableProcess(int i) {
        
         try { 
             if(i==3){
            ResultSet rs = getResult();
            while (rs.next()) {
                
                String id_contract = rs.getString("id_contract");
                String description = rs.getString("description");
                String id_lecturer = rs.getString("id_lecturer");
                String id_subject = rs.getString("id_subject");
                String start_date = rs.getString("start_date");
                String end_date = rs.getString("end_date");
                boolean status = rs.getBoolean("status");

                Vector v = new Vector();

                v.add(id_contract);
                v.add(description);
                v.add(id_subject);
                v.add(id_lecturer);
                v.add(start_date);
                v.add(end_date);
                if (status) {
                    v.add("Active");
                } else {
                    v.add("Inactive");
                }

                dtm1 = (DefaultTableModel) jTable3.getModel();
                dtm1.addRow(v);
            }

            }else if(i==2){
                ResultSet rs = getResult();
                
                while (rs.next()) {
                    
                String id_class = rs.getString("id_class");
                String description = rs.getString("description");
                String id_contract = rs.getString("id_contract");
                String class_fees = String.valueOf(rs.getDouble("class_fees"));
                boolean status = rs.getBoolean("status");

                Vector v = new Vector();

                v.add(id_class);
                v.add(description);
                v.add(id_contract);
                v.add(class_fees);
                if (status) {
                    v.add("Active");
                } else {
                    v.add("Inactive");
                }

                dtm1 = (DefaultTableModel) jTable2.getModel();
                dtm1.addRow(v);
            }
            }
             
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void genarateClassId() {
        try {
            ResultSet rs = DB.search("select count(*) as cls_count from class");
            if (rs.next()) {
                int count = rs.getInt("cls_count");
                String classid = "AECLSS" + (++count);
                txt_classId.setText(classid);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    

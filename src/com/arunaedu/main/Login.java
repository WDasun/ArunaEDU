    private void txt_passwordKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_passwordKeyReleased
       
        if(evt.getKeyCode()==10){
            try{
            if(STATUSSEARCH("system_user", "user_name", txt_username.getText())){
              String username = txt_username.getText();
            String password = new String(txt_password.getPassword());
            
            Connection connection = DB.getDBConnection();
            String sql ="select * from system_user where user_name=? and password=?";
            PreparedStatement ps = connection.prepareStatement(sql);
            ps.setString(1, username);
            String encPw =MD5.getMd5(password);
            ps.setString(2, encPw);
            ResultSet rs = ps.executeQuery();
            
            if(rs.next()){
                SystemConfigData.setActiveUser(username);
                SystemConfigData.setUserType(rs.getString("id_usertype"));
                SystemConfigData.setEmployee(rs.getString("user_name"));
                
                loading();
            
            }else{
                JOptionPane.showMessageDialog(this, "Invalid Username or Password");
            }
            }else{
                JOptionPane.showMessageDialog(this, "User not Active !");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        }
    }//GEN-LAST:event_txt_passwordKeyReleased


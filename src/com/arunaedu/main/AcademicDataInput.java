    private void fillExamTable() {
        try {

            ResultSet rs = getResult();
            while (rs.next()) {

                String id_exam = rs.getString("id_exam");
                String description = rs.getString("description");
                String id_class = rs.getString("id_class");

                Vector v = new Vector();

                v.add(id_exam);
                v.add(description);
                v.add(id_class);

                dtm1 = (DefaultTableModel) jTable3.getModel();
                dtm1.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void examSearch(int type) {
        try {
            if (type == 0) {
                SEARCH("shedule_exam");
                fillExamTable();
            } else if (type == 1) {
                LIKESEARCH("shedule_exam", "id_exam", txt_examid.getText());
                fillExamTable();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


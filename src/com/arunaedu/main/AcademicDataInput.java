    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        if (!txt_examid.getText().equals("") && !txt_examStudentid.getText().equals("") && !txt_examScore.getText().equals("")) {
            int status = (rad_active.isSelected()) ? 1 : 0;
            try {
                DB.push("insert into student_exam (id_exam,id_student,score,attends_status) values ('"
                        + txt_examid.getText() + "','"
                        + txt_examStudentid.getText() + "','"
                        + Double.parseDouble(txt_examScore.getText()) + "','"
                        + status + "')");
                clearExamFileds();
                JOptionPane.showMessageDialog(this, "Saved !");
            } catch (Exception e) {
                e.printStackTrace();
            }

        } else {
            JOptionPane.showMessageDialog(this, "FILL ALL FIELDS !");
        }
    }//GEN-LAST:event_btn_saveActionPerformed


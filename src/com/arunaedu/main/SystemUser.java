    private void loadUsers() {

           try {
               removeAllRows(jTable1);
            ResultSet rs = DB.search("select user_name,id_usertype,status from system_user");
            while (rs.next()) {
                
                String user_name = rs.getString("user_name");
                String id_usertype = rs.getString("id_usertype");

                boolean status = rs.getBoolean("status");

                Vector v = new Vector();

                v.add(user_name);
                v.add(id_usertype);

                if (status) {
                    v.add("Active");
                } else {
                    v.add("Inactive");
                }

                dtm1 = (DefaultTableModel) jTable1.getModel();
                dtm1.addRow(v);  
        }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
        }
            
    }
    
     private void removeAllRows(JTable j) {
        for (int i = j.getRowCount() - 1; i >= 0; i--) {
            ((DefaultTableModel) j.getModel()).removeRow(i);
        }
    }
     
     private void loadUserTypes(){
     
         try {
             ResultSet rs = DB.search("select * from syuser_type");
             while(rs.next()){
                 Vector v = new Vector();
                 v.add(rs.getString("id_usertype"));
                 v.add(rs.getString("description"));
                 
                dtm1 = (DefaultTableModel) jTable2.getModel();
                dtm1.addRow(v);
             }
         } catch (Exception e) {
             JOptionPane.showMessageDialog(this, e);
         }
     
     }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            

           if(txt_username.getText()!="" && txt_usertype.getText()!=""){
            String username = txt_username.getText();
            String usertypeid = txt_usertype.getText();
            String password = new String(txt_password.getPassword());
            String repassword = new String(txt_password_repeate.getPassword());
            
            int activestatus = 0;
            if(rad_active.isSelected()){
                activestatus = 1;
            }else{
                activestatus = 0;
            }
            
            if(password.equals(repassword)){
                String encPw =MD5.getMd5(password);
                DB.push("insert into system_user values('"+username+"','"+usertypeid+"','"+encPw+"','"+activestatus+"')");
                JOptionPane.showMessageDialog(this, "Saved !");
                clearField();
                loadUsers();
            }else{
                JOptionPane.showMessageDialog(this, "Passwords not matching");
            }
           }else{
               JOptionPane.showMessageDialog(this, "Fill All The Fields !");
           }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      try {
           
            String username = txt_username.getText();
            String password = new String(txt_password.getPassword());
            String repassword = new String(txt_password_repeate.getPassword());
            String usertype = txt_usertype.getText();
            int activestatus = (rad_active.isSelected()) ? 1 : 0;
            
            if(password.equals(repassword)){
                String encPw =MD5.getMd5(password);
                DB.push("update system_user set password='"+encPw+"',user_name='"+username+"',id_usertype='"+usertype+"',status='"+activestatus+"' where user_name='"+username+"'");
                JOptionPane.showMessageDialog(this, "Updated !");
                clearField();
                loadUsers();
            }else{
                JOptionPane.showMessageDialog(this, "Passwords not matching");
            }

            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

